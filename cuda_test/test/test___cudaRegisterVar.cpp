#include <cuda_runtime.h>
#include <UPTK_runtime.h>
#include <stdio.h>

#define CHECK_CUDA(call) \
    do { \
        UPTKError_t err = call; \
        if (err != UPTKSuccess) { \
            printf("CUDA error at %s:%d: %s\n", __FILE__, __LINE__, \
                   UPTKGetErrorString(err)); \
            return 1; \
        } \
    } while (0)

int main() {
    int deviceCount;
    UPTKGetDeviceCount(&deviceCount);
    if (deviceCount == 0) {
        printf("test_skip: no CUDA device available\n");
        return 0;
    }
    UPTKSetDevice(0);

    // __cudaRegisterVar is an internal compiler function
    // not exposed in the public CUDA Runtime API headers.
    // It is used by the compiler to register device variables.
    // Cannot be called directly from user code.
    printf("test_skip: __cudaRegisterVar is an internal compiler function, not callable from user code\n");
    return 0;
}
