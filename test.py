# ===================== 测试执行函数 =====================
def run_test(bin_path):
    try:
        ret = subprocess.run([bin_path], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, timeout=10)
        return ret.returncode == 0
    except:
        return False

def run_module(module_name, test_dir, test_list):
    print("=" * 70)
    print(f"               {module_name} 测试")
    print("=" * 70)

    total = len(test_list)
    passed = 0
    failed = []

    for test in test_list:
        path = os.path.join(test_dir, test)
        if not os.path.exists(path):
            print(f"❌ {test} -> 不存在")
            failed.append(test)
            continue

        print(f"\n▶ 运行: {test}")
        ok = run_test(path)
        if ok:
            print(f"✅ 通过")
            passed += 1
        else:
            print(f"❌ 失败")
            failed.append(test)

    print("\n" + "=" * 70)
    print(f"                     {module_name} 报告")
    print("=" * 70)
    print(f"📌 总用例：{total}")
    print(f"✅ 通过：{passed}")
    print(f"❌ 失败：{len(failed)}")
    print(f"📊 通过率：{passed/total*100:.2f}%" if total else "📊 通过率：0%")
    if mode == "runtime":
        print(f"📈 覆盖率(based on 365):{passed/365*100:.2f}%")
    elif mode == "cublas":
        print(f"📈 覆盖率(based on 343):{passed/343*100:.2f}%")
    elif mode == "cufft":
        print(f"📈 覆盖率(based on 53):{passed/53*100:.2f}%")
    elif mode == "nccl":
        print(f"📈 覆盖率(based on 19):{passed/TOTAL_API_NCCL*100:.2f}%")
    else:
        sys.exit(1)
    print("=" * 70)

    if failed:
        print("\n失败用例列表：")
        for f in failed:
            print(f"  ❌ {f}")

    return len(failed)

