if __name__ == "__main__":
    if len(sys.argv) != 2:
        print("用法：")
        print("  python3 test.py runtime   # 只跑 Runtime")
        print("  python3 test.py cublas    # 只跑 CUBLAS")
        print("  python3 test.py cufft     # 只跑 cuFFT")
